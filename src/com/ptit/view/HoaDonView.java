    private void jButtonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveActionPerformed
        // TODO add your handling code here:
        slist.remove(selected);
        this.jButtonRemove.setVisible(false);
        if (slist.isEmpty()) {
            this.jLabelTongTien.setVisible(false);
            this.jLabelGetTongTien.setVisible(false);
        } else {
            this.jLabelTongTien.setVisible(true);
            this.jLabelGetTongTien.setVisible(true);
        }
        Float tongTien = 0.0f;
        model = (DefaultTableModel) this.jTableNCC.getModel();
        model.setRowCount(0);
        for (HoadonLinhkien item : slist) {
            tongTien += item.getThanhTien();
            model.addRow(new Object[]{
                item.getMa(), item.getTen(), item.getHang(), item.getDonGia(), item.getSoLuong(), item.getThanhTien()
            });
        }
        this.jLabelGetTongTien.setText(String.valueOf(tongTien));
    }//GEN-LAST:event_jButtonRemoveActionPerformed

