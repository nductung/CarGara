    private void jButtonInHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInHoaDonActionPerformed
        // TODO add your handling code here:
        if (slist.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng thêm ít nhất một linh kiện");
        } else {
            int input = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn in hoá đơn?");
            if (input == 0) {
                int status = hoaDonDAO.createHoaDon(nhanvien.getID(), nhaCungCap.getID(), slist);
                if (status == 0) {
                    JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi");
                } else {
                    JOptionPane.showMessageDialog(this, "Đã thêm vào cơ sở dữ liệu");
                    this.setVisible(false);
                    HomePageView homePageView = new HomePageView(nhanvien);
                    homePageView.setVisible(true);
                    homePageView.setTitle("Hệ thống quản lý gara ô tô");
                }
            }
        }
    }//GEN-LAST:event_jButtonInHoaDonActionPerformed

